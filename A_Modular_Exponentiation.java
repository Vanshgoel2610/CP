import java.util.Scanner;

public class A_Modular_Exponentiation {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        if(n > 26) System.out.println(m);
        else System.out.println((int)(m%Math.pow(2, n)));
        sc.close();
    }
}